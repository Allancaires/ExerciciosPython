import math

angulo = float(input('Digite o angulo que você deseja: '))
print(f'O ângulo de {angulo} tem SENO de {math.sin(math.radians(angulo)):.2f}')
print(f'O ângulo de {angulo} tem COSSENO de {math.cos(math.radians(angulo)):.2f}')
print(f'O ângulo de {angulo} tem TANGENTE de {math.tan(math.radians(angulo)):.2f}')
